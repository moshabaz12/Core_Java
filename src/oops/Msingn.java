package oops;

class Example {
    void sum(int a, int b) {   // Method signature: sum(int, int)
        System.out.println(a + b);
    }

    void sum(double a, double b) {  // Method signature: sum(double, double)
        System.out.println(a + b);
    }

    int multiply(int x, int y) {    // Method signature: multiply(int, int)
        return x * y;
    }
}
